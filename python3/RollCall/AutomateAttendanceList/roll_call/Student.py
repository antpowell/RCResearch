class Student:

    def set_course_no(self, course_no):
        self.course_no = course_no

    def set_tnumber(self, t_num):
        self.t_num = t_num

    def set_last_name(self, l_name):
        self.l_name = l_name

    def set_password(self, password):
        self.password = password

    def set_sms_time(self, sms_time):
        self.sms_time = sms_time
